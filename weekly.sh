#!/bin/bash
# ============================================================
# weekly.sh — Еженедельный обзор прогресса
# Запуск: ./scripts/weekly.sh  (раз в неделю, в любой день)
# ============================================================

# --- НАСТРОЙКИ ---
PROJECT_DIR="$HOME/code/projects/Aristotel"
MEMORY_FILE="$PROJECT_DIR/MEMORY.md"
PROGRESS_FILE="$PROJECT_DIR/PROGRESS.md"
REVIEWS_DIR="$PROJECT_DIR/reviews"
# -----------------

set -e
cd "$PROJECT_DIR"

mkdir -p "$REVIEWS_DIR"

WEEK_NUM=$(date +%Y-W%V)
REVIEW_FILE="$REVIEWS_DIR/review_${WEEK_NUM}.md"
TODAY=$(date +%d.%m.%Y)

echo ""
echo "📊 Еженедельный обзор прогресса в Ликее"
echo "   Неделя $WEEK_NUM"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo ""
echo "🤖 Агент анализирует прошедшую неделю..."
echo ""

git pull origin main --quiet

claude --dangerously-skip-permissions --print \
"Ты — агент анализа философского прогресса. Составь еженедельный отчёт.

ДАННЫЕ:
- Прочитай MEMORY.md — извлеки последние 7 записей (или все, если их меньше 7)
- Прочитай PROGRESS.md — проверь статусы всех концепций

СОСТАВЬ отчёт и сохрани в файл reviews/review_${WEEK_NUM}.md:

---
# Обзор недели $WEEK_NUM — $TODAY

## Пройдено за неделю
[список Дней с темами и свитками — кратко, одной строкой на день]

## Что усвоено хорошо ✅
[концепции со статусом ✅ в PROGRESS.md, появившиеся на этой неделе]

## Что требует повторения 👁
[концепции со статусом 👁 — упоминались, но не разбирались глубоко]

## Ещё не касались ⬜
[важные концепции Аристотеля которых нет в PROGRESS.md:
 проверь список: Сущность (οὐσία), Движущий ум, Категории,
 Силлогизм, Дружба (φιλία), Полития, Катарсис, Мимесис, Время, Место]

## Рекомендации на следующую неделю
[3 конкретные темы с обоснованием]

## Вопрос для самопроверки
[один сложный вопрос по пройденному материалу]
---

После создания файла выведи краткое резюме:
- Сколько дней пройдено за неделю
- Топ-3 усвоенных концепции
- Главная рекомендация на следующую неделю"

echo ""
if [ -f "$REVIEW_FILE" ]; then
    git add "$REVIEWS_DIR/"
    git commit -m "Обзор недели $WEEK_NUM"
    git push origin main --quiet
    echo "✓ Обзор сохранён: reviews/review_${WEEK_NUM}.md"
    echo "✓ Запушено в GitHub"
else
    echo "⚠️  Файл обзора не создан. Проверь агента."
fi

echo ""
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "📚 Мудрость прирастает постепенно."
echo "   До следующей прогулки по Ликею!"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo ""
