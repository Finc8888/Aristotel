#!/bin/bash
# ============================================================
# morning.sh — Утренняя подготовка к беседе с Аристотелем
# Запуск: ./scripts/morning.sh
# ============================================================

# --- НАСТРОЙКИ (измени под свои пути) ---
PROJECT_DIR="$HOME/code/projects/Aristotel"
METAPHYSICS_FILE="$PROJECT_DIR/metaphysics.md"
TODAY_SCROLL="$PROJECT_DIR/today_scroll.md"
MEMORY_FILE="$PROJECT_DIR/MEMORY.md"
PROGRESS_FILE="$PROJECT_DIR/PROGRESS.md"
# -----------------------------------------

set -e
cd "$PROJECT_DIR"

echo ""
echo "🏛  Ликей просыпается..."
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"

# 1. Проверка кодировки файла Метафизики
if [ ! -f "$METAPHYSICS_FILE" ]; then
    # Ищем исходный .txt файл и конвертируем
    TXT_FILE=$(find "$PROJECT_DIR" -maxdepth 1 -name "*.txt" | head -1)
    if [ -n "$TXT_FILE" ]; then
        echo "🔄 Конвертирую $(basename $TXT_FILE) из Windows-1251 в UTF-8..."
        iconv -f windows-1251 -t utf-8 "$TXT_FILE" -o "$METAPHYSICS_FILE"
        echo "   ✓ Сохранено как metaphysics.md"
    else
        echo "⚠️  Файл metaphysics.md не найден. Свиток будет создан без текста."
    fi
else
    echo "📖 metaphysics.md найден"
fi

# 2. Синхронизация с GitHub
echo "📥 Подтягиваю изменения с GitHub..."
git pull origin main --quiet
echo "   ✓ Синхронизировано"

# 3. Зафиксировать номер дня до начала сессии
SESSION_DAY_FILE="$PROJECT_DIR/.session_day"
if [ -f "$MEMORY_FILE" ]; then
    LAST_DAY=$(grep -oP '(?<=### День )\d+' "$MEMORY_FILE" | tail -1)
    SESSION_DAY=$((LAST_DAY + 1))
else
    SESSION_DAY=1
fi
echo "$SESSION_DAY" > "$SESSION_DAY_FILE"
echo "📅 Сегодня День $SESSION_DAY"

# 4. Показать последнюю запись из памяти
echo ""
echo "📜 Последняя беседа:"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
if [ -f "$MEMORY_FILE" ]; then
    tail -8 "$MEMORY_FILE"
else
    echo "   (память пуста — это первая беседа)"
fi
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"

# 5. Агент готовит свиток
echo ""
echo "🤖 Агент готовит свиток для сегодняшней беседы..."
echo ""

claude --dangerously-skip-permissions --print \
"Ты — агент подготовки к философской беседе. Выполни строго по шагам:

1. Прочитай MEMORY.md — найди последнюю запись (последний День N).
   Извлеки: какой свиток читали, что разбирали, что намечено на завтра.

2. Прочитай PROGRESS.md (если существует) — найди концепции со статусом 👁 или ⬜.

3. На основе этого определи тему: приоритет — поле «Завтра» из последней записи MEMORY.md.

4. Найди в metaphysics.md следующую главу.
   Например если предыдущий свиток была глава [Метафизика, Книга III, Глава 3]
   То следующая глава будет [Метафизика, Книга III, Глава 4] или [Метафизика, Книга IV, Глава 1].
   Если эта глава не соответствует ранне определенной теме на сегоднящний день упомяни это,
   но продолжнай обсуждать то что в свитке.
   Если есть связь между ими упомяни это и используй эту связь как пример для обсуждения нового свитка

5. Создай файл today_scroll.md строго в этом формате:
---
# Свиток на $(date +%d.%m.%Y)

## Тема: [название темы]
## Источник: [Метафизика, Книга X, Глава Y]

## Текст отрывка:
[найденный отрывок из metaphysics.md]

## Ключевые понятия:
[2–3 термина Аристотеля из этого отрывка]

## Связь с прошлой беседой:
[одно предложение]
---

6. После создания файла выведи только: 'Свиток готов: [тема]'"

# 6. Проверка результата
echo ""
if [ -f "$TODAY_SCROLL" ]; then
    echo "✅ Свиток готов:"
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
    head -6 "$TODAY_SCROLL"
    echo "..."
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
else
    echo "⚠️  Свиток не создан автоматически."
    echo "   Вставь текст отрывка вручную в начале беседы."
fi

# 7. Открыть беседу
echo ""
echo "🌅 Доброе утро, друг мой. Ликей ждёт."
echo ""
sleep 1

claude
